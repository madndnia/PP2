x = 5
print(type(x))
int

x = "Hello World"
print(type(x))
str

x = 20.5
print(type(x))
float

x = ["apple", "banana", "cherry"]    #list - упорядоченная коллекция элементов, можно изменить после создания списка, элементы могут повторяться
print(type(x))
list

x = ("apple", "banana", "cherry")  #tuple(кортеж) - упорядоченная коллекция элементов, нельзя изменить после создания списка, элементы могут повторяться
print(type(x))
tuple

x = {"name" : "John", "age" : 36}    #set(множество) - неупорядоченная!! коллекция элементов, нельзя вызывать по индексу, элементы не могут повторяться
print(type(x))
dict

x = True
print(type(x))
bool




